// PaddingMode — Cryptographic Padding Schemes
// =============================================================================

/// Cryptographic padding modes for symmetric and asymmetric operations.
///
/// Replaces C integer constants such as `RSA_PKCS1_PADDING`,
/// `RSA_PKCS1_OAEP_PADDING`, `RSA_PKCS1_PSS_PADDING`, and the
/// `EVP_PADDING_*` family.
///
/// # Examples
///
/// ```
/// use openssl_common::types::PaddingMode;
///
/// let mode = PaddingMode::Pkcs7;
/// assert_ne!(mode, PaddingMode::None);
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum PaddingMode {
    /// No padding.  The caller is responsible for ensuring the plaintext
    /// length is an exact multiple of the block size.
    None,
    /// PKCS#7 padding (RFC 5652 § 6.3).  Default for CBC-mode block ciphers.
    Pkcs7,
    /// OAEP with SHA-1 (`RSA_PKCS1_OAEP_PADDING` with default MGF).
    OaepSha1,
    /// OAEP with SHA-256 hash and MGF1-SHA-256.
    OaepSha256,
    /// RSA-PSS probabilistic signature padding (RFC 8017 § 8.1).
    Pss,
    /// ISO 10126 padding — random fill with length byte at the end.
    Iso10126,
    /// ANSI X9.23 padding — zero fill with length byte at the end.
    Ansi923,
}

impl fmt::Display for PaddingMode {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let label = match self {
            Self::None => "none",
            Self::Pkcs7 => "PKCS#7",
            Self::OaepSha1 => "OAEP-SHA1",
            Self::OaepSha256 => "OAEP-SHA256",
            Self::Pss => "PSS",
            Self::Iso10126 => "ISO-10126",
            Self::Ansi923 => "ANSI-X9.23",
        };
        f.write_str(label)
    }
}

