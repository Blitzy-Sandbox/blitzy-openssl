// KeyType — Cryptographic Key Algorithm Families
// =============================================================================

/// Identifies the algorithm family of a cryptographic key.
///
/// Replaces the C `EVP_PKEY_RSA`, `EVP_PKEY_EC`, etc. integer constants
/// and provides methods for NID mapping and classification into asymmetric
/// vs. symmetric and classical vs. post-quantum categories.
///
/// # Examples
///
/// ```
/// use openssl_common::types::{KeyType, Nid};
///
/// let kt = KeyType::Ed25519;
/// assert!(kt.is_asymmetric());
/// assert!(!kt.is_post_quantum());
/// assert_eq!(kt.to_nid(), Nid::ED25519);
///
/// let pq = KeyType::MlKem768;
/// assert!(pq.is_post_quantum());
/// assert!(pq.is_asymmetric());
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum KeyType {
    /// RSA key (`NID_rsaEncryption = 6`).
    Rsa,
    /// RSA-PSS key (`NID_rsassaPss = 912`).
    RsaPss,
    /// Diffie-Hellman key (`NID_dhKeyAgreement = 28`).
    Dh,
    /// DSA key (`NID_dsa = 116`).
    Dsa,
    /// Elliptic-curve key — generic NIST / Brainpool curves
    /// (`NID_X9_62_id_ecPublicKey = 408`).
    Ec,
    /// Ed25519 signing key (`NID_ED25519 = 1087`).
    Ed25519,
    /// Ed448 signing key (`NID_ED448 = 1088`).
    Ed448,
    /// X25519 key-agreement key (`NID_X25519 = 1034`).
    X25519,
    /// X448 key-agreement key (`NID_X448 = 1035`).
    X448,
    /// ML-KEM-512 post-quantum KEM, FIPS 203 (`NID_ML_KEM_512 = 1454`).
    MlKem512,
    /// ML-KEM-768 post-quantum KEM, FIPS 203 (`NID_ML_KEM_768 = 1455`).
    MlKem768,
    /// ML-KEM-1024 post-quantum KEM, FIPS 203 (`NID_ML_KEM_1024 = 1456`).
    MlKem1024,
    /// ML-DSA-44 post-quantum signature, FIPS 204 (`NID_ML_DSA_44 = 1457`).
    MlDsa44,
    /// ML-DSA-65 post-quantum signature, FIPS 204 (`NID_ML_DSA_65 = 1458`).
    MlDsa65,
    /// ML-DSA-87 post-quantum signature, FIPS 204 (`NID_ML_DSA_87 = 1459`).
    MlDsa87,
    /// SLH-DSA (Stateless Hash-Based Digital Signature Algorithm), FIPS 205.
    /// Represented generically — specific parameter sets distinguished at
    /// a higher level.  Uses `NID_SLH_DSA_SHA2_128s = 1460` as the canonical NID.
    SlhDsa,
    /// LMS (Leighton-Micali Signature), SP 800-208.
    /// Uses `NID_id_alg_hss_lms_hashsig = 1501` as the canonical NID.
    Lms,
    /// HMAC symmetric key (`NID_hmac = 855`).
    Hmac,
    /// CMAC symmetric key (`NID_cmac = 894`).
    Cmac,
}

/// NID constant for RSA-PSS (`NID_rsassaPss`).
const NID_RSA_PSS: Nid = Nid(912);
/// NID constant for Diffie-Hellman (`NID_dhKeyAgreement`).
const NID_DH: Nid = Nid(28);
/// NID constant for DSA (`NID_dsa`).
const NID_DSA: Nid = Nid(116);
/// NID constant for Ed448 (`NID_ED448`).
const NID_ED448: Nid = Nid(1088);
/// NID constant for X448 (`NID_X448`).
const NID_X448: Nid = Nid(1035);
/// NID constant for ML-KEM-512 (`NID_ML_KEM_512`).
const NID_ML_KEM_512: Nid = Nid(1454);
/// NID constant for ML-KEM-1024 (`NID_ML_KEM_1024`).
const NID_ML_KEM_1024: Nid = Nid(1456);
/// NID constant for ML-DSA-44 (`NID_ML_DSA_44`).
const NID_ML_DSA_44: Nid = Nid(1457);
/// NID constant for ML-DSA-65 (`NID_ML_DSA_65`).
const NID_ML_DSA_65: Nid = Nid(1458);
/// NID constant for ML-DSA-87 (`NID_ML_DSA_87`).
const NID_ML_DSA_87: Nid = Nid(1459);
/// NID constant for SLH-DSA (`NID_SLH_DSA_SHA2_128s` — canonical representative).
const NID_SLH_DSA: Nid = Nid(1460);
/// NID constant for LMS/HSS (`NID_id_alg_hss_lms_hashsig`).
const NID_LMS: Nid = Nid(1501);
/// NID constant for HMAC (`NID_hmac`).
const NID_HMAC: Nid = Nid(855);
/// NID constant for CMAC (`NID_cmac`).
const NID_CMAC: Nid = Nid(894);

impl KeyType {
    /// Maps this key type to its canonical [`Nid`].
    ///
    /// The mapping matches the C `EVP_PKEY_*` → `NID_*` correspondence
    /// defined across `include/openssl/evp.h` and `include/openssl/obj_mac.h`.
    #[inline]
    pub const fn to_nid(self) -> Nid {
        match self {
            Self::Rsa => Nid::RSA,
            Self::RsaPss => NID_RSA_PSS,
            Self::Dh => NID_DH,
            Self::Dsa => NID_DSA,
            Self::Ec => Nid::EC,
            Self::Ed25519 => Nid::ED25519,
            Self::Ed448 => NID_ED448,
            Self::X25519 => Nid::X25519,
            Self::X448 => NID_X448,
            Self::MlKem512 => NID_ML_KEM_512,
            Self::MlKem768 => Nid::ML_KEM_768,
            Self::MlKem1024 => NID_ML_KEM_1024,
            Self::MlDsa44 => NID_ML_DSA_44,
            Self::MlDsa65 => NID_ML_DSA_65,
            Self::MlDsa87 => NID_ML_DSA_87,
            Self::SlhDsa => NID_SLH_DSA,
            Self::Lms => NID_LMS,
            Self::Hmac => NID_HMAC,
            Self::Cmac => NID_CMAC,
        }
    }

    /// Attempts to determine the [`KeyType`] from a [`Nid`].
    ///
    /// Returns [`None`] if the NID does not correspond to any known key type
    /// (Rule R5 — no sentinel values).
    #[inline]
    pub const fn from_nid(nid: Nid) -> Option<Self> {
        match nid.0 {
            6 => Some(Self::Rsa),          // NID_rsaEncryption
            912 => Some(Self::RsaPss),     // NID_rsassaPss
            28 => Some(Self::Dh),          // NID_dhKeyAgreement
            116 => Some(Self::Dsa),        // NID_dsa
            408 => Some(Self::Ec),         // NID_X9_62_id_ecPublicKey
            1087 => Some(Self::Ed25519),   // NID_ED25519
            1088 => Some(Self::Ed448),     // NID_ED448
            1034 => Some(Self::X25519),    // NID_X25519
            1035 => Some(Self::X448),      // NID_X448
            1454 => Some(Self::MlKem512),  // NID_ML_KEM_512
            1455 => Some(Self::MlKem768),  // NID_ML_KEM_768
            1456 => Some(Self::MlKem1024), // NID_ML_KEM_1024
            1457 => Some(Self::MlDsa44),   // NID_ML_DSA_44
            1458 => Some(Self::MlDsa65),   // NID_ML_DSA_65
            1459 => Some(Self::MlDsa87),   // NID_ML_DSA_87
            1460 => Some(Self::SlhDsa),    // NID_SLH_DSA_SHA2_128s
            1501 => Some(Self::Lms),       // NID_id_alg_hss_lms_hashsig
            855 => Some(Self::Hmac),       // NID_hmac
            894 => Some(Self::Cmac),       // NID_cmac
            _ => None,
        }
    }

    /// Returns `true` if this key type belongs to an asymmetric (public-key)
    /// algorithm.
    ///
    /// Symmetric-only key types ([`KeyType::Hmac`], [`KeyType::Cmac`]) return
    /// `false`.
    #[inline]
    pub const fn is_asymmetric(self) -> bool {
        match self {
            Self::Hmac | Self::Cmac => false,
            Self::Rsa
            | Self::RsaPss
            | Self::Dh
            | Self::Dsa
            | Self::Ec
            | Self::Ed25519
            | Self::Ed448
            | Self::X25519
            | Self::X448
            | Self::MlKem512
            | Self::MlKem768
            | Self::MlKem1024
            | Self::MlDsa44
            | Self::MlDsa65
            | Self::MlDsa87
            | Self::SlhDsa
            | Self::Lms => true,
        }
    }

    /// Returns `true` if this key type is a post-quantum algorithm.
    ///
    /// Post-quantum key types are ML-KEM (FIPS 203), ML-DSA (FIPS 204),
    /// SLH-DSA (FIPS 205), and LMS (SP 800-208).
    #[inline]
    pub const fn is_post_quantum(self) -> bool {
        match self {
            Self::MlKem512
            | Self::MlKem768
            | Self::MlKem1024
            | Self::MlDsa44
            | Self::MlDsa65
            | Self::MlDsa87
            | Self::SlhDsa
            | Self::Lms => true,
            Self::Rsa
            | Self::RsaPss
            | Self::Dh
            | Self::Dsa
            | Self::Ec
            | Self::Ed25519
            | Self::Ed448
            | Self::X25519
            | Self::X448
            | Self::Hmac
            | Self::Cmac => false,
        }
    }
}

