//! Shared type definitions for the OpenSSL Rust workspace.
//!
//! Provides newtype wrappers, algorithm identifiers ([`Nid`]), protocol version
//! constants ([`ProtocolVersion`]), padding modes ([`PaddingMode`]), key types
//! ([`KeyType`]), cipher modes ([`CipherMode`]), operation types
//! ([`OperationType`]), and the [`AlgorithmName`] trait — all foundational types
//! shared across every workspace crate.
//!
//! This module replaces the C forward declarations in `include/openssl/types.h`
//! (249 lines of opaque `typedef struct … *` forward declarations), the
//! compatibility aliases in `include/openssl/ossl_typ.h`, the core dispatch
//! types in `include/openssl/core.h`, and the integer `#define NID_*` constant
//! groups from `include/openssl/obj_mac.h`.
//!
//! # Design Principles
//!
//! - **Rule R5 (Nullability):** Every fallible conversion returns
//!   [`Option<T>`] instead of a sentinel value such as `0`, `-1`, or `""`.
//! - **Rule R6 (Lossless Casts):** The [`Nid`] newtype preserves the raw
//!   `i32` from C but all conversions use explicit methods (`as_raw`,
//!   `from_raw`), never bare `as` casts.
//! - **Rule R8 (Zero Unsafe):** This module contains zero `unsafe` blocks.
//! - **Rule R9 (Warning-Free):** Every public item carries a `///` doc comment
//!   and every enum variant is documented.
//!
//! # Feature Flag Convention
//!
//! Feature flags map to Cargo features, replacing C preprocessor
//! `OPENSSL_NO_*` guards:
//!
//! ```text
//! C:    #ifndef OPENSSL_NO_EC
//! Rust: #[cfg(feature = "ec")]
//! ```
//!
//! Standard features: `ec`, `rsa`, `dh`, `dsa`, `chacha`, `des`, `sm2`, `sm3`,
//! `sm4`, `cms`, `ocsp`, `ct`, `ts`, `cmp`, `hpke`, `quic`, `ech`, `fips`,
//! `legacy`.
//!
//! # Migration Reference
//!
//! | C Construct                        | Rust Equivalent                 |
//! |------------------------------------|---------------------------------|
//! | `NID_*` integer `#define` (obj_mac.h) | [`Nid`] newtype constants   |
//! | `SSL3_VERSION` / `TLS1_3_VERSION`  | [`ProtocolVersion`] enum        |
//! | `RSA_PKCS1_PADDING` etc.           | [`PaddingMode`] enum            |
//! | `EVP_PKEY_RSA` / `EVP_PKEY_EC`     | [`KeyType`] enum                |
//! | `EVP_CIPH_GCM_MODE` etc.           | [`CipherMode`] enum             |
//! | `OSSL_OP_DIGEST` etc.              | [`OperationType`] enum          |
//! | Forward `typedef struct … *`       | Concrete Rust structs elsewhere |

use serde::{Deserialize, Serialize};
use std::fmt;

// =============================================================================
// Nid — Numeric Identifier for ASN.1 Objects
// =============================================================================

/// Numeric identifier (NID) for ASN.1 objects and algorithm names.
///
/// In the C codebase, NIDs are plain `int` constants defined via `#define NID_*`
/// in `include/openssl/obj_mac.h` (generated by `crypto/objects/objects.pl`).
/// There are approximately 1 200 unique NIDs.  This newtype wrapper preserves
/// the raw `i32` value for FFI compatibility while providing a type-safe
/// interface for Rust code.
///
/// Only the most commonly referenced NIDs are declared here as associated
/// constants.  The full NID catalogue lives in the `openssl-crypto` crate
/// alongside OID-to-NID lookup tables.
///
/// # Examples
///
/// ```
/// use openssl_common::types::Nid;
///
/// let sha256 = Nid::SHA256;
/// assert_eq!(sha256.as_raw(), 672);
/// assert!(!sha256.is_undef());
///
/// let undef = Nid::UNDEF;
/// assert!(undef.is_undef());
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord, Serialize, Deserialize)]
pub struct Nid(pub i32);

impl Nid {
    // ── Special sentinel ────────────────────────────────────────────────
    /// Undefined / unknown NID.  Corresponds to `NID_undef` (0).
    pub const UNDEF: Self = Self(0);

    // ── Digest algorithms ───────────────────────────────────────────────
    /// MD5 message digest (`NID_md5 = 4`).
    pub const MD5: Self = Self(4);
    /// SHA-1 message digest (`NID_sha1 = 64`).
    pub const SHA1: Self = Self(64);
    /// SHA-256 message digest (`NID_sha256 = 672`).
    pub const SHA256: Self = Self(672);
    /// SHA-384 message digest (`NID_sha384 = 673`).
    pub const SHA384: Self = Self(673);
    /// SHA-512 message digest (`NID_sha512 = 674`).
    pub const SHA512: Self = Self(674);
    /// SHA3-256 message digest (`NID_sha3_256 = 1097`).
    pub const SHA3_256: Self = Self(1097);
    /// SHA3-384 message digest (`NID_sha3_384 = 1098`).
    pub const SHA3_384: Self = Self(1098);
    /// SHA3-512 message digest (`NID_sha3_512 = 1099`).
    pub const SHA3_512: Self = Self(1099);

    // ── Cipher algorithms ───────────────────────────────────────────────
    /// AES-128-GCM authenticated encryption (`NID_aes_128_gcm = 895`).
    pub const AES_128_GCM: Self = Self(895);
    /// AES-256-GCM authenticated encryption (`NID_aes_256_gcm = 901`).
    pub const AES_256_GCM: Self = Self(901);
    /// ChaCha20-Poly1305 AEAD (`NID_chacha20_poly1305 = 1018`).
    pub const CHACHA20_POLY1305: Self = Self(1018);

    // ── Asymmetric key types ────────────────────────────────────────────
    /// RSA public-key algorithm (`NID_rsaEncryption = 6`).
    pub const RSA: Self = Self(6);
    /// Elliptic-curve public key (`NID_X9_62_id_ecPublicKey = 408`).
    pub const EC: Self = Self(408);
    /// Ed25519 signature algorithm (`NID_ED25519 = 1087`).
    pub const ED25519: Self = Self(1087);
    /// X25519 key-agreement algorithm (`NID_X25519 = 1034`).
    pub const X25519: Self = Self(1034);

    // ── Post-quantum algorithms ─────────────────────────────────────────
    /// ML-KEM-768 post-quantum KEM, FIPS 203 (`NID_ML_KEM_768 = 1455`).
    pub const ML_KEM_768: Self = Self(1455);

    // ── Accessors ───────────────────────────────────────────────────────

    /// Returns the raw `i32` value of this NID.
    ///
    /// Useful for serialization, FFI boundary crossings, and lookup tables.
    #[inline]
    pub const fn as_raw(self) -> i32 {
        self.0
    }

    /// Constructs a [`Nid`] from a raw `i32` value.
    ///
    /// No validation is performed — any `i32` is accepted.  Use the
    /// associated constants for well-known NIDs.
    #[inline]
    pub const fn from_raw(nid: i32) -> Self {
        Self(nid)
    }

    /// Returns `true` if this NID is the undefined sentinel ([`Nid::UNDEF`]).
    #[inline]
    pub const fn is_undef(self) -> bool {
        self.0 == Self::UNDEF.0
    }
}

impl fmt::Display for Nid {
    /// Formats the NID as `NID(<value>)` for human-readable output.
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "NID({})", self.0)
    }
}

// =============================================================================
// ProtocolVersion — TLS/DTLS Protocol Version Identifiers
// =============================================================================

/// TLS and DTLS protocol version identifiers.
///
/// In C these are `#define` constants such as `SSL3_VERSION 0x0300` and
/// `TLS1_3_VERSION 0x0304` (see `include/openssl/prov_ssl.h`).  DTLS versions
/// use the bitwise-complement encoding defined in RFC 6347 § 4.1 where a
/// *higher* DTLS version has a *lower* numeric value.
///
/// # Rule R5 (Nullability)
///
/// [`ProtocolVersion::from_raw`] returns [`Option<ProtocolVersion>`] rather
/// than a sentinel value for unrecognised wire values.
///
/// # Examples
///
/// ```
/// use openssl_common::types::ProtocolVersion;
///
/// let tls13 = ProtocolVersion::Tls1_3;
/// assert_eq!(tls13.as_raw(), 0x0304);
/// assert!(tls13.is_tls());
/// assert!(!tls13.is_dtls());
///
/// assert!(ProtocolVersion::from_raw(0xFFFF).is_none());
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, PartialOrd, Ord, Serialize, Deserialize)]
#[repr(u16)]
pub enum ProtocolVersion {
    /// SSL 3.0 (`SSL3_VERSION = 0x0300`).  Deprecated — retained for
    /// downgrade detection and legacy interop testing.
    Ssl3_0 = 0x0300,
    /// TLS 1.0 (`TLS1_VERSION = 0x0301`).
    Tls1_0 = 0x0301,
    /// TLS 1.1 (`TLS1_1_VERSION = 0x0302`).
    Tls1_1 = 0x0302,
    /// TLS 1.2 (`TLS1_2_VERSION = 0x0303`).
    Tls1_2 = 0x0303,
    /// TLS 1.3 (`TLS1_3_VERSION = 0x0304`).
    Tls1_3 = 0x0304,
    /// DTLS 1.0 (`DTLS1_VERSION = 0xFEFF`).
    Dtls1_0 = 0xFEFF,
    /// DTLS 1.2 (`DTLS1_2_VERSION = 0xFEFD`).
    Dtls1_2 = 0xFEFD,
}

impl ProtocolVersion {
    /// Returns the on-the-wire `u16` value of this protocol version.
    #[inline]
    pub const fn as_raw(self) -> u16 {
        self as u16
    }

    /// Attempts to convert a raw `u16` wire value into a known
    /// [`ProtocolVersion`].
    ///
    /// Returns [`None`] for unrecognised values (Rule R5).
    #[inline]
    pub const fn from_raw(value: u16) -> Option<Self> {
        match value {
            0x0300 => Some(Self::Ssl3_0),
            0x0301 => Some(Self::Tls1_0),
            0x0302 => Some(Self::Tls1_1),
            0x0303 => Some(Self::Tls1_2),
            0x0304 => Some(Self::Tls1_3),
            0xFEFF => Some(Self::Dtls1_0),
            0xFEFD => Some(Self::Dtls1_2),
            _ => None,
        }
    }

    /// Returns `true` if this version belongs to the DTLS protocol family.
    ///
    /// DTLS versions are identified by having the high byte set to `0xFE`
    /// (RFC 6347 § 4.1).
    #[inline]
    pub const fn is_dtls(self) -> bool {
        (self.as_raw() >> 8) == 0xFE
    }

    /// Returns `true` if this version belongs to the TLS (or SSL) protocol
    /// family.
    ///
    /// All TLS/SSL versions have a high byte of `0x03`.
    #[inline]
    pub const fn is_tls(self) -> bool {
        (self.as_raw() >> 8) == 0x03
    }
}

impl fmt::Display for ProtocolVersion {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let label = match self {
            Self::Ssl3_0 => "SSLv3",
            Self::Tls1_0 => "TLSv1.0",
            Self::Tls1_1 => "TLSv1.1",
            Self::Tls1_2 => "TLSv1.2",
            Self::Tls1_3 => "TLSv1.3",
            Self::Dtls1_0 => "DTLSv1.0",
            Self::Dtls1_2 => "DTLSv1.2",
        };
        f.write_str(label)
    }
}

