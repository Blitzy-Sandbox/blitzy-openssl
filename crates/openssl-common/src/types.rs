/// Block cipher modes of operation.
///
/// Replaces the C `EVP_CIPH_ECB_MODE`, `EVP_CIPH_CBC_MODE`, … constants
/// defined in `include/openssl/evp.h` and the `EVP_CIPH_*_MODE` flag family.
///
/// # Examples
///
/// ```
/// use openssl_common::types::CipherMode;
///
/// let gcm = CipherMode::Gcm;
/// assert_ne!(gcm, CipherMode::Ecb);
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum CipherMode {
    /// Electronic Codebook (ECB) mode.
    Ecb,
    /// Cipher Block Chaining (CBC) mode.
    Cbc,
    /// Cipher Feedback (CFB) mode.
    Cfb,
    /// Output Feedback (OFB) mode.
    Ofb,
    /// Counter (CTR) mode.
    Ctr,
    /// Galois/Counter Mode (GCM) — authenticated encryption.
    Gcm,
    /// Counter with CBC-MAC (CCM) — authenticated encryption.
    Ccm,
    /// XEX-based Tweaked-codebook mode with ciphertext Stealing (XTS).
    Xts,
    /// AES Key Wrap (RFC 3394).
    Wrap,
    /// AES Key Wrap with Padding (RFC 5649).
    WrapPad,
    /// Offset Codebook Mode (OCB) — authenticated encryption.
    Ocb,
    /// Synthetic Initialization Vector (SIV) mode (RFC 5297).
    Siv,
    /// AES-GCM-SIV (RFC 8452) — nonce-misuse-resistant AEAD.
    GcmSiv,
    /// Stream cipher mode, e.g. `ChaCha20` or `RC4`.
    Stream,
}

impl fmt::Display for CipherMode {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let label = match self {
            Self::Ecb => "ECB",
            Self::Cbc => "CBC",
            Self::Cfb => "CFB",
            Self::Ofb => "OFB",
            Self::Ctr => "CTR",
            Self::Gcm => "GCM",
            Self::Ccm => "CCM",
            Self::Xts => "XTS",
            Self::Wrap => "WRAP",
            Self::WrapPad => "WRAP-PAD",
            Self::Ocb => "OCB",
            Self::Siv => "SIV",
            Self::GcmSiv => "GCM-SIV",
            Self::Stream => "STREAM",
        };
        f.write_str(label)
    }
}

// =============================================================================
// OperationType — Provider Operation Categories
// =============================================================================

/// Categories of cryptographic operations dispatched through the provider
/// framework.
///
/// Replaces the C `OSSL_OP_DIGEST`, `OSSL_OP_CIPHER`, … integer constants
/// defined in `include/openssl/core_dispatch.h`.  Each variant corresponds
/// to a set of dispatch function IDs that a provider must implement to
/// supply the operation.
///
/// # Examples
///
/// ```
/// use openssl_common::types::OperationType;
///
/// let op = OperationType::Digest;
/// assert_ne!(op, OperationType::Cipher);
/// ```
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum OperationType {
    /// Message digest / hash operations (`OSSL_OP_DIGEST`).
    Digest,
    /// Symmetric cipher operations (`OSSL_OP_CIPHER`).
    Cipher,
    /// Message Authentication Code operations (`OSSL_OP_MAC`).
    Mac,
    /// Key Derivation Function operations (`OSSL_OP_KDF`).
    Kdf,
    /// Random number generation operations (`OSSL_OP_RAND`).
    Rand,
    /// Key management operations (`OSSL_OP_KEYMGMT`).
    KeyMgmt,
    /// Digital signature operations (`OSSL_OP_SIGNATURE`).
    Signature,
    /// Asymmetric cipher operations (`OSSL_OP_ASYM_CIPHER`).
    AsymCipher,
    /// Key Encapsulation Mechanism operations (`OSSL_OP_KEM`).
    Kem,
    /// Key exchange / agreement operations (`OSSL_OP_KEYEXCH`).
    KeyExch,
    /// Encoder/decoder (serialization) operations (`OSSL_OP_ENCODER` /
    /// `OSSL_OP_DECODER`).
    EncoderDecoder,
    /// Object store operations (`OSSL_OP_STORE`).
    Store,
    /// Symmetric (opaque/secret) key management operations (`OSSL_OP_SKEYMGMT`).
    ///
    /// Distinct from [`KeyMgmt`](Self::KeyMgmt) which handles asymmetric key
    /// management.  In C OpenSSL, `OSSL_OP_STORE` and `OSSL_OP_SKEYMGMT` are
    /// separate operation types dispatched independently by the provider
    /// framework.
    SKeyMgmt,
}

impl fmt::Display for OperationType {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        let label = match self {
            Self::Digest => "digest",
            Self::Cipher => "cipher",
            Self::Mac => "mac",
            Self::Kdf => "kdf",
            Self::Rand => "rand",
            Self::KeyMgmt => "keymgmt",
            Self::Signature => "signature",
            Self::AsymCipher => "asymcipher",
            Self::Kem => "kem",
            Self::KeyExch => "keyexch",
            Self::EncoderDecoder => "encoder_decoder",
            Self::Store => "store",
            Self::SKeyMgmt => "skeymgmt",
        };
        f.write_str(label)
    }
}

// =============================================================================
// AlgorithmName — Trait for Named Algorithms
// =============================================================================

/// Trait for types that represent a named cryptographic algorithm.
///
/// Implementors provide a human-readable algorithm name and its
/// corresponding [`Nid`].  This is used by the provider framework
/// to register and look up algorithm implementations by name.
///
/// # Examples
///
/// ```
/// use openssl_common::types::{AlgorithmName, Nid};
///
/// struct Sha256Digest;
///
/// impl AlgorithmName for Sha256Digest {
///     fn algorithm_name(&self) -> &'static str { "SHA2-256" }
///     fn nid(&self) -> Nid { Nid::SHA256 }
/// }
///
/// let d = Sha256Digest;
/// assert_eq!(d.algorithm_name(), "SHA2-256");
/// assert_eq!(d.nid(), Nid::SHA256);
/// ```
pub trait AlgorithmName {
    /// Returns the canonical algorithm name string.
    ///
    /// The returned name should match the string used in provider
    /// `OSSL_ALGORITHM` registration tables (e.g. `"SHA2-256"`, `"AES-128-GCM"`).
    fn algorithm_name(&self) -> &'static str;

    /// Returns the numeric identifier ([`Nid`]) for this algorithm.
    fn nid(&self) -> Nid;
}

