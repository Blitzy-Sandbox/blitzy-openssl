/// Constructs the common set of algorithm parameters for a cipher.
///
/// Returns a list of `(key, value)` pairs describing the cipher's
/// fundamental properties: mode, key length, IV length, block size,
/// AEAD capability, and padding status.
///
/// Translates the C `ossl_cipher_generic_get_params()` function from
/// `providers/implementations/ciphers/ciphercommon.c`.
///
/// # Parameters
///
/// - `mode`: The cipher operating mode.
/// - `key_len`: Key length in bytes.
/// - `iv_len`: IV/nonce length in bytes.
/// - `block_size`: Block size in bytes (1 for stream ciphers).
/// - `flags`: Cipher capability flags.
pub fn generic_get_params(
    mode: CipherMode,
    key_len: usize,
    iv_len: usize,
    block_size: usize,
    flags: CipherFlags,
) -> Vec<(&'static str, String)> {
    let mut params = Vec::new();
    params.push((param_keys::MODE, mode.to_string()));
    params.push((param_keys::KEYLEN, key_len.to_string()));
    params.push((param_keys::IVLEN, iv_len.to_string()));
    params.push((param_keys::BLOCK_SIZE, block_size.to_string()));
    params.push((
        param_keys::AEAD,
        if flags.contains(CipherFlags::AEAD) { "1" } else { "0" }.to_string(),
    ));
    params.push((
        param_keys::PADDING,
        if mode == CipherMode::Ecb || mode == CipherMode::Cbc { "1" } else { "0" }.to_string(),
    ));
    params.push((
        param_keys::CUSTOM_IV,
        if flags.contains(CipherFlags::CUSTOM_IV) { "1" } else { "0" }.to_string(),
    ));
    params
}

/// Validates and initializes a cipher key.
///
/// Checks that the provided key length matches the expected key length
/// for the cipher. Returns the validated key material as a byte vector.
///
/// Translates part of `ossl_cipher_generic_initkey()` from
/// `providers/implementations/ciphers/ciphercommon.c`.
///
/// # Parameters
///
/// - `key`: The raw key material.
/// - `expected_len`: The expected key length in bytes.
///
/// # Returns
///
/// - `Ok(Vec<u8>)` with the validated key bytes.
/// - `Err(String)` if the key length doesn't match.
pub fn generic_init_key(key: &[u8], expected_len: usize) -> Result<Vec<u8>, String> {
    if key.len() != expected_len {
        return Err(format!(
            "invalid key length: expected {expected_len} bytes, got {} bytes",
            key.len()
        ));
    }
    Ok(key.to_vec())
}

/// Processes a block-mode cipher update for data that must be block-aligned.
///
/// Buffers partial blocks and processes complete blocks. Remaining partial
/// block data is returned for the caller to buffer.
///
/// Translates the block-update logic from `ossl_cipher_generic_block_update()`
/// in `providers/implementations/ciphers/ciphercommon.c`.
///
/// # Parameters
///
/// - `input`: Input data to process.
/// - `block_size`: The cipher block size in bytes.
/// - `pending`: Previously buffered partial block data.
///
/// # Returns
///
/// A tuple of `(complete_blocks, remaining)`:
/// - `complete_blocks`: Data consisting of complete blocks ready for processing.
/// - `remaining`: Leftover partial block data to be buffered.
pub fn generic_block_update(
    input: &[u8],
    block_size: usize,
    pending: &[u8],
) -> (Vec<u8>, Vec<u8>) {
    let mut combined = Vec::with_capacity(pending.len() + input.len());
    combined.extend_from_slice(pending);
    combined.extend_from_slice(input);

    let full_blocks_len = (combined.len() / block_size) * block_size;
    let remaining = combined[full_blocks_len..].to_vec();
    combined.truncate(full_blocks_len);
    (combined, remaining)
}

/// Processes a stream-mode cipher update (no block alignment required).
///
/// Stream ciphers process data byte-by-byte, so all input is immediately
/// available for processing without buffering.
///
/// Translates the stream-update logic from `ossl_cipher_generic_stream_update()`
/// in `providers/implementations/ciphers/ciphercommon.c`.
///
/// # Parameters
///
/// - `input`: Input data to process.
///
/// # Returns
///
/// A copy of the input data ready for processing.
pub fn generic_stream_update(input: &[u8]) -> Vec<u8> {
    input.to_vec()
}

/// Validates a GCM authentication tag length.
///
/// GCM tags must be between 4 and 16 bytes (inclusive) and must be even.
/// The standard tag length is 16 bytes (128 bits).
///
/// Translates the tag length validation from `ossl_gcm_setiv()` in
/// `providers/implementations/ciphers/ciphercommon_gcm.c`.
///
/// # Parameters
///
/// - `tag_len`: The requested tag length in bytes.
///
/// # Returns
///
/// - `Ok(())` if the tag length is valid.
/// - `Err(String)` describing why the tag length is invalid.
pub fn gcm_validate_tag_len(tag_len: usize) -> Result<(), String> {
    if !(4..=16).contains(&tag_len) {
        return Err(format!("GCM tag length must be 4..=16 bytes, got {tag_len}"));
    }
    if tag_len % 2 != 0 {
        return Err(format!("GCM tag length must be even, got {tag_len}"));
    }
    Ok(())
}

/// Validates a CCM authentication tag length.
///
/// CCM tags must be between 4 and 16 bytes (inclusive) and must be even.
///
/// Translates the tag length validation from CCM operations in
/// `providers/implementations/ciphers/ciphercommon_ccm.c`.
///
/// # Parameters
///
/// - `tag_len`: The requested tag length in bytes.
///
/// # Returns
///
/// - `Ok(())` if the tag length is valid.
/// - `Err(String)` describing why the tag length is invalid.
pub fn ccm_validate_tag_len(tag_len: usize) -> Result<(), String> {
    if !(4..=16).contains(&tag_len) {
        return Err(format!("CCM tag length must be 4..=16 bytes, got {tag_len}"));
    }
    if tag_len % 2 != 0 {
        return Err(format!("CCM tag length must be even, got {tag_len}"));
    }
    Ok(())
}

/// Generates a random IV of the specified length.
///
/// Uses `rand::RngCore::fill_bytes()` from the OS CSPRNG to produce
/// cryptographically secure random IV bytes.
///
/// Translates the IV generation logic from `ossl_cipher_generic_initiv()`
/// in `providers/implementations/ciphers/ciphercommon.c`.
///
/// # Parameters
///
/// - `iv_len`: The desired IV length in bytes.
///
/// # Returns
///
/// A `Vec<u8>` containing `iv_len` random bytes.
pub fn generate_random_iv(iv_len: usize) -> Vec<u8> {
    use rand::RngCore;
    let mut iv = vec![0u8; iv_len];
    rand::thread_rng().fill_bytes(&mut iv);
    iv
}

/// Verifies an AEAD authentication tag against an expected value.
///
/// Performs a constant-time comparison to prevent timing side-channel
/// attacks. Both tags must have the same length.
///
/// Translates the tag verification from AEAD finalization routines in
/// `providers/implementations/ciphers/ciphercommon_gcm.c` and
/// `ciphercommon_ccm.c`.
///
/// # Parameters
///
/// - `computed`: The tag computed during decryption.
/// - `expected`: The expected tag provided by the caller.
///
/// # Returns
///
/// - `Ok(())` if the tags match.
/// - `Err(String)` if the tags don't match or have different lengths.
pub fn verify_tag(computed: &[u8], expected: &[u8]) -> Result<(), String> {
    use subtle::ConstantTimeEq;
    if computed.len() != expected.len() {
        return Err(format!(
            "tag length mismatch: computed {} bytes, expected {} bytes",
            computed.len(),
            expected.len()
        ));
    }
    if computed.ct_eq(expected).into() {
        Ok(())
    } else {
        Err("AEAD tag verification failed".to_string())
    }
}
