/// State machine for GCM (Galois/Counter Mode) cipher operations.
///
/// Tracks the progression of a GCM encryption or decryption operation
/// through its required phases: initialization → AAD processing → data
/// processing → finalization.
///
/// Replaces the implicit state tracking in C's `PROV_GCM_CTX` from
/// `providers/implementations/ciphers/ciphercommon_gcm.c`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum GcmState {
    /// Context created but not yet initialized with key and IV.
    Uninitialized,
    /// Key and IV have been set; ready to process AAD or plaintext.
    Initialized,
    /// Processing additional authenticated data (AAD).
    ProcessingAad,
    /// Processing plaintext/ciphertext data.
    ProcessingData,
    /// Operation finalized; tag has been generated (encrypt) or verified (decrypt).
    Finalized,
    /// An error occurred; context is no longer usable.
    Error,
}

/// State machine for CCM (Counter with CBC-MAC) cipher operations.
///
/// CCM requires the total plaintext length to be known before processing
/// begins, which introduces an additional `LengthSet` state compared to GCM.
///
/// Replaces the implicit state tracking in C's `PROV_CCM_CTX` from
/// `providers/implementations/ciphers/ciphercommon_ccm.c`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum CcmState {
    /// Context created but not yet initialized.
    Uninitialized,
    /// Key and IV have been set; waiting for length information.
    Initialized,
    /// Total data length has been specified; ready for AAD or data.
    LengthSet,
    /// Processing additional authenticated data (AAD).
    ProcessingAad,
    /// Processing plaintext/ciphertext data.
    ProcessingData,
    /// Operation finalized; tag generated or verified.
    Finalized,
    /// An error occurred; context is no longer usable.
    Error,
}

